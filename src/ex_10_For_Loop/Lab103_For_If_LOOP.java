package ex_10_For_Loop;

public class Lab103_For_If_LOOP {
    public static void main(String[] args) {
        for(int shruti=1; shruti < 18; shruti++){ // 0 to 17, 18 times
            if (shruti > 15){
                System.out.println("Shruti age is "+shruti+". Gift from Papa, IPhone");
            }
            else {
                System.out.println("Shruti age is "+shruti+". So No Mobile Phone Gift");
            }
        }
    }
}
