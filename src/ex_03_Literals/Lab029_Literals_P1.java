package ex_03_Literals;

public class Lab029_Literals_P1 {
    public static void main(String[] args) {
        int age = 65;
        // Literal - Integral
        System.out.println(age);

        final float pi = 3.14f;
        //  Literal - float
        System.out.println(pi);

        final int girl_age = 18;
        //girl_age = 19;
        System.out.println(girl_age);
    }
}
